    private void loadData() throws SQLException {
//        dskh = ConnectSQL.getKhachHangs();
        ArrayList<KhachHang> ds = new ArrayList<>();
        ds = buss.getKhachHangs();
        tablemodel.setRowCount(0);
        int i = 1;
        String gioitinh;
        for (KhachHang kh : ds) {
            Vector vctData = new Vector();
            vctData.add(i);
            i++;
            vctData.add(kh.getId());
            vctData.add(kh.getHo());
            vctData.add(kh.getTen());
            vctData.add(kh.getDiachi());
            vctData.add(kh.getPhone());
//            vctData.add(kh.isGioitinh());
            if (kh.isGioitinh()) {
                gioitinh = "Nam";
            } else {
                gioitinh = "Nữ";
            }

            vctData.add(gioitinh);
//            vctData.add(kh.getDia_chi());

            tablemodel.addRow(vctData);

        }
    }


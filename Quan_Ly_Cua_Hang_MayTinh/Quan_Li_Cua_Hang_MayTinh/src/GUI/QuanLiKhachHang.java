    private void jButton_SuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_SuaActionPerformed

        int i = jTable_QLKH.getSelectedRow();
        boolean gt = false;
        if (jRadioButton_Nam.isSelected()) {
            gt = true;
        }
        if (jTextField_Ten.getText().trim().equals("") || jTextField_SoDienThoai.getText().trim().equals("") || jTextField_DiaChi.getText().trim().equals("") || jTextField_Ho.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Các trường không được bỏ trống");

        } else {
            try {
                if (buss.checkSDTKH(jTextField_SoDienThoai.getText())) {
                    KhachHang kh = new KhachHang(Integer.parseInt(jTextField_idkh.getText()), jTextField_Ho.getText(), jTextField_Ten.getText(), jTextField_DiaChi.getText(), jTextField_SoDienThoai.getText(), gt);
                    buss.getDskh().get(i).update(jTextField_Ho.getText(), jTextField_Ten.getText(), jTextField_DiaChi.getText(), jTextField_SoDienThoai.getText(), gt);
                    try {
                        loadData();
                    } catch (SQLException ex) {
                        Logger.getLogger(QuanLiKhachHang.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    try {
                        boolean x = buss.updateKH(kh);
                        if (x) {
                            JOptionPane.showMessageDialog(rootPane, "Cập nhật thành  công");
                            loadData();
                        }
                    } catch (SQLException ex) {
                        Logger.getLogger(QuanLiKhachHang.class.getName()).log(Level.SEVERE, null, ex);
                    }
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Số Điện Thoại đã tồn tại");

                }
            } catch (SQLException ex) {
                Logger.getLogger(QuanLiKhachHang.class.getName()).log(Level.SEVERE, null, ex);
            }
        }


    }//GEN-LAST:event_jButton_SuaActionPerformed


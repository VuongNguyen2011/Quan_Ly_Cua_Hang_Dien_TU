    private void jButton_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_themActionPerformed

        if (jTextField_Ten.getText().trim().equals("") || jTextField_SoDienThoai.getText().trim().equals("") || jTextField_DiaChi.getText().trim().equals("") || jTextField_Ho.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Các trường không được bỏ trống");

        } else {
            try {
                boolean gt = false;
                if (jRadioButton_Nam.isSelected()) {
                    gt = true;
                }
                if (buss.checkSDTKH(jTextField_SoDienThoai.getText())) {
                    KhachHang kh = new KhachHang(jTextField_Ho.getText(), jTextField_Ten.getText(), jTextField_DiaChi.getText(), jTextField_SoDienThoai.getText(), gt);
                    boolean x = buss.insertKhachHang(kh);
                    if (x) {
                        JOptionPane.showMessageDialog(rootPane, "Thêm thành  công");
                        loadData();
                    }
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Số Điện Thoại Đã tồn tại");

                }
            } catch (SQLException ex) {
                Logger.getLogger(QuanLiKhachHang.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(QuanLiKhachHang.class.getName()).log(Level.SEVERE, null, ex);
            }
        }


    }//GEN-LAST:event_jButton_themActionPerformed

